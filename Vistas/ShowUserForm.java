    public void recibeDatos(int idEmp, String nombreEmp, String apellidos, String TipoDocumento, String documento, String correo){
        System.out.println("idEmp: " + idEmp + ", nombre: " + nombreEmp + ", apellidos:  " + apellidos + ", correo " + correo);
        txtId.setText("" + idEmp);
        txtNombre.setText(nombreEmp);
        txtApellidos.setText(apellidos);
        txtTipoDocumento.setText(TipoDocumento);
        txtDocumento.setText(documento);
        txtCorreo.setText(correo);
    }
    
    //5. Creamos el metodo que actualizara el nombre, apellidos y correo usuario
    public void actualizarEmpleado() {
        int idEmp = Integer.parseInt(txtId.getText());
        String documento = txtDocumento.getText();
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        String correo = txtCorreo.getText();
        if (nombre.isEmpty()){
            JOptionPane.showMessageDialog(this, "El nombre del empleado es un campo requerido" ,"", JOptionPane.WARNING_MESSAGE);    
        }else if (apellidos.isEmpty()){
            JOptionPane.showMessageDialog(this, "El valor del campo apellidos es un campo requerido" ,"", JOptionPane.WARNING_MESSAGE);
        }else if (correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "El correo del empleado es un campo requerido" ,"", JOptionPane.WARNING_MESSAGE);     
        }else {
            String query = "UPDATE `empleados` SET `nombreEmp`='" + nombre + "',`apellidos`='" + apellidos + "',"
                    + "`correo`='" + correo + " ' WHERE `idEmp`='" + idEmp + "'";
            System.out.println(query);
            
            
            try {
                connection = conexion.getConnection();
                st = connection.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"Se han actualizado los datos del empleado", "", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this,"No fue posible actualizar los datos del empleado", "", JOptionPane.ERROR_MESSAGE);
                }
            this.dispose();
            
        }
    }
    
    
     private void eliminarEmpleado (){
        //1. Consultamos el ID del empleado
        int idEmp = Integer.parseInt(txtId.getText());
        //2. Validar si el usuario no ha seleccionado un empleado
        String query = "DELETE FROM `empleados` WHERE IdEmp = " + idEmp + ";";
         System.out.println(query);
         try{
             connection = conexion.getConnection();
             st = connection.createStatement();
             st.executeUpdate(query);
             JOptionPane.showMessageDialog(this, "El usuario ha sido eliminado ");
         }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "No se pudo eliminar el empleado");
        }
         this.dispose();
       }

